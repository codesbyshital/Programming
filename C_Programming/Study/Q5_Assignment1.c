#include <stdio.h>

/////////////////////////////////////////////////////////////////////////////////////
//
// Function Name :  Accept
// Output:          5 times *
// Description:     Assignment_1 : Q.5: Accept one number from user and print that number of * on screen.
// Date:            22/05/2026
// Author:          Shital Ajit Nikam   
//
/////////////////////////////////////////////////////////////////////////////////////


void Accept(int iNo)
{
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo ; iCnt++)
    {
        printf("*");
    }
    
}

int main()
{
    int iValue = 0;
        
    printf("Enter number\n");
    scanf("%d",&iValue);

    Accept(iValue);

    return 0;
}


