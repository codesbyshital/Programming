void Display(int iNo)
{
    int iCnt = 0;

    //Updater used
    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iCnt < iNo)
    {
        printf("*");
        iCnt++;
    }    
}

int main()
{
    int iValue = 0;
        
    printf("Enter number\n");
    scanf("%d",&iValue);

    Display(iValue);

    return 0;
}

