#include <stdio.h>

/////////////////////////////////////////////////////////////////////////////////////
//
// Function Name :  Display
// Output:          5 times Marvellous
// Description:     Assignment_1 : Q.2: Program to print 5 times "Marvellous" on Screen.
// Date:            22/05/2026
